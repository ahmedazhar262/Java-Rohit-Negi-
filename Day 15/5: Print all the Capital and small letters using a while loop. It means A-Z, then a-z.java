// Print all the Capital and small letters using a while loop. It
// means A-Z, then a-z.


import java.util.*;
public class Capital_Letter{
    public static void main(String [] args){
        
        char c= 'A';
        while(c<='Z'){
            System.out.println(c);
            c++;
            
        }
          char d= 'a';
        while(d<='z'){
            System.out.println(d);
            d++;
        }
      
    }
}
